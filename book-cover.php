<script>
(function () {
  var GAP      = 22;
  var viewport = document.getElementById('revViewport');
  var track    = document.getElementById('revTrack');
  var dotsEl   = document.getElementById('revDots');
  var countEl  = document.getElementById('revCount');
  var cards    = track.querySelectorAll('.rev-card');
  var total    = cards.length;
  var cur      = 0;

  function getCols() {
    var w = window.innerWidth;
    return w >= 1024 ? 3 : w >= 600 ? 2 : 1;
  }

  function setWidths() {
    var cols  = getCols();
    var cardW = Math.floor((viewport.offsetWidth - GAP * (cols - 1)) / cols);
    [].forEach.call(cards, function (c) { c.style.width = cardW + 'px'; });
    return cardW;
  }

  function buildDots() {
    var max = total - getCols();
    dotsEl.innerHTML = '';
    for (var i = 0; i <= max; i++) {
      var d = document.createElement('button');
      d.className = 'rev-dot' + (i === cur ? ' on' : '');
      d.setAttribute('aria-label', 'Review ' + (i + 1));
      (function (n) { d.addEventListener('click', function () { go(n); }); })(i);
      dotsEl.appendChild(d);
    }
  }

  function go(idx) {
    var cols  = getCols();
    var max   = total - cols;
    cur = ((idx % (max + 1)) + (max + 1)) % (max + 1);
    var cardW  = setWidths();
    track.style.transform = 'translateX(-' + cur * (cardW + GAP) + 'px)';
    dotsEl.querySelectorAll('.rev-dot').forEach(function (d, i) {
      d.classList.toggle('on', i === cur);
    });
    countEl.textContent = (cur + 1) + ' / ' + (max + 1);
  }

  function init() {
    setWidths();
    buildDots();
    go(cur);
  }

  document.getElementById('revPrev').addEventListener('click', function () { go(cur - 1); });
  document.getElementById('revNext').addEventListener('click', function () { go(cur + 1); });

  var sx = 0;
  viewport.addEventListener('touchstart', function (e) { sx = e.touches[0].clientX; }, { passive: true });
  viewport.addEventListener('touchend',   function (e) {
    var dx = e.changedTouches[0].clientX - sx;
    if (Math.abs(dx) > 40) go(dx < 0 ? cur + 1 : cur - 1);
  });

  var t = setInterval(function () { go(cur + 1); }, 5000);
  viewport.addEventListener('mouseenter', function () { clearInterval(t); });
  viewport.addEventListener('mouseleave', function () { t = setInterval(function () { go(cur + 1); }, 5000); });

  var rt;
  window.addEventListener('resize', function () {
    clearTimeout(rt);
    rt = setTimeout(function () { cur = Math.min(cur, total - getCols()); init(); }, 150);
  });

  document.readyState === 'loading'
    ? document.addEventListener('DOMContentLoaded', init)
    : setTimeout(init, 80);
})();
</script>


